class Up(nn.Module):
    """Upscaling then double conv"""

    def __init__(self, in_channels, out_channels, bilinear=True):
        super().__init__()

        # if bilinear, use the normal convolutions to reduce the number of channels
        if bilinear:
            self.up = nn.Upsample(scale_factor=2, mode='bilinear', align_corners=True)
            self.convLstm = ConvLSTM(in_channels // 2, out_channels, (3, 3), 1, True, True, False)
        else:
            self.up = nn.ConvTranspose2d(in_channels, in_channels // 2, kernel_size=2, stride=2)
            self.convLstm = ConvLSTM(in_channels // 2, out_channels, (3, 3), 1, True, True, False)

    def forward(self, x1, x2):
        x1 = self.up(x1)
        # input is CHW
        diffY = x2.size()[2] - x1.size()[2]
        diffX = x2.size()[3] - x1.size()[3]

        x1 = F.pad(x1, [diffX // 2, diffX - diffX // 2,
                        diffY // 2, diffY - diffY // 2])

        # if you have padding issues, see
        # https://github.com/HaiyongJiang/U-Net-Pytorch-Unstructured-Buggy/commit/0e854509c2cea854e247a9c615f175f76fbb2e3a
        # https://github.com/xiaopeng-liao/Pytorch-UNet/commit/8ebac70e633bac59fc22bb5195e513d5832fb3bd
        x = torch.cat([x2, x1], dim=1)
        x = torch.cat([x2.unsqueeze(0), x1.unsqueeze(0)], dim=0)
        x = x.permute(1, 0, 2, 3, 4)
        _, x = self.convLstm(x)
        x = x[0][0]
        return x


class OutConv(nn.Module):
    def __init__(self, in_channels, out_channels):
        super(OutConv, self).__init__()
        self.conv = nn.Conv2d(in_channels, out_channels, kernel_size=1)

    def forward(self, x):
        return self.conv(x)
        
        
class UMF_ConvLSTM(nn.Module):
    def __init__(self, n_channels, n_classes, bilinear=True, type_net=1):
        super(UMF_ConvLSTM, self).__init__()
        self.n_channels = n_channels
        self.n_classes = n_classes
        self.bilinear = bilinear

        self.inc = DoubleConv(n_channels, int(64/type_net))
        self.down1 = Down(int(64/type_net), int(128/type_net))
        self.down2 = Down(int(128/type_net), int(256/type_net))
        self.down3 = Down(int(256/type_net), int(512/type_net))
        factor = 2 if bilinear else 1
        self.down4 = Down4(int(512/type_net), int(1024/type_net) // factor)
        self.up1 = Up(int(1024/type_net), int(512/type_net) // factor, bilinear)
        self.up2 = Up(int(512/type_net), int(256/type_net) // factor, bilinear)
        self.up3 = Up(int(256/type_net), int(128/type_net) // factor, bilinear)
        self.up4 = Up(int(128/type_net), int(64/type_net), bilinear)
        self.outc = OutConv(int(64/type_net), n_classes)

    def forward(self, x):
        x1 = self.inc(x)
        x2 = self.down1(x1)
        x3 = self.down2(x2)
        x4 = self.down3(x3)
        x5 = self.down4(x4)

        x = self.up1(x5, x4)
        x = self.up2(x, x3)
        x = self.up3(x, x2)
        x = self.up4(x, x1)
        logits = self.outc(x)
        return logits
